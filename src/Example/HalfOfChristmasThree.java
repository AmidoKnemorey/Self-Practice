package Example;

public class HalfOfChristmasThree {

    public static void main(String[] args) {
        int quantityOfRows = 5;
        char sign = '*';
        byte sign2 = 0;
        for (int i = 1; i <= quantityOfRows; i++) {
            for (int j = 0; j <= sign2; j++) {
                System.out.print(sign);
            }
            System.out.println();
            sign2++;
        } } }
